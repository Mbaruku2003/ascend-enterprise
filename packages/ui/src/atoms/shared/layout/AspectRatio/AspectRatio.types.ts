import type { ReactNode } from "react";

import type { BoxProps } from "../Box";

/* -------------------------------------------------------------------------- */
/* Ratio                                                                      */
/* -------------------------------------------------------------------------- */

export type AspectRatioValue =
  | number
  | "1/1"
  | "4/3"
  | "3/2"
  | "16/9"
  | "21/9";

/* -------------------------------------------------------------------------- */
/* Props                                                                      */
/* -------------------------------------------------------------------------- */

export interface AspectRatioProps
  extends Omit<BoxProps, "children"> {
  /**
   * Aspect ratio.
   *
   * @default "16/9"
   */
  ratio?: AspectRatioValue;

  children?: ReactNode;
}
