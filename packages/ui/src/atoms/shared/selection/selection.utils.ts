/**
 * ============================================================================
 * Ascend UI
 * Shared Selection Utilities
 * ============================================================================
 */

/* -------------------------------------------------------------------------- */
/* Toggle                                                                     */
/* -------------------------------------------------------------------------- */

export function toggleSelection<T>(
  values: readonly T[],
  value: T,
): T[] {
  return values.includes(value)
    ? values.filter(
        item => item !== value,
      )
    : [...values, value];
}

/* -------------------------------------------------------------------------- */
/* Contains                                                                   */
/* -------------------------------------------------------------------------- */

export function isSelected<T>(
  values: readonly T[],
  value: T,
): boolean {
  return values.includes(value);
}

/* -------------------------------------------------------------------------- */
/* Normalize                                                                  */
/* -------------------------------------------------------------------------- */

export function normalizeSelection<T>(
  value?: readonly T[],
): T[] {
  return value
    ? [...value]
    : [];
}

/* -------------------------------------------------------------------------- */
/* Single Selection                                                           */
/* -------------------------------------------------------------------------- */

export function selectOne<T>(
  value: T,
): T[] {
  return [value];
}

/* -------------------------------------------------------------------------- */
/* Clear                                                                      */
/* -------------------------------------------------------------------------- */

export function clearSelection<T>(): T[] {
  return [];
}

/* -------------------------------------------------------------------------- */
/* Equality                                                                   */
/* -------------------------------------------------------------------------- */

export function selectionEquals<T>(
  a: readonly T[],
  b: readonly T[],
): boolean {
  if (a.length !== b.length) {
    return false;
  }

  return a.every(value => b.includes(value));
}
